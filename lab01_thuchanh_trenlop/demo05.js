let num = 100;
console.log("num = num + 1 =>", num + 1);
console.log("num = num - 1 =>", num - 1);
console.log("num = num * 2 =>", num * 2);
console.log("num = num / 2 =>", num / 2);
console.log("num = num / 3 =>", num / 3);
console.log("num = num % 3 =>", num % 3);
console.log("num = num / 3 =>", parseInt(num / 3));

num  = 100;
console.log("num++ =>", num++);
console.log("num", num)
console.log("++num =>", ++num);
console.log("num-- =>", num--);
console.log("--num =>", --num);
//num++ in num ra trước mới cộng sau

num  = 100;

num += 5;
console.log("num += 5", num);
num -= 4;
console.log("num -= 4", num);
num *= 2;
console.log("num *= 2", num);
num /= 6;
