/**
 * So sánh dữ liệu 
 */

console.log("1 == 1 =>",1 == 1);
console.log("1.0 == 1 =>",1.0 == 1);
console.log("1 == '1'=>",1 == '1');
console.log("1.0 == '1'=>",1.0 == '1');
console.log ("-------------------------");
console.log("1 === 1 =>",1 === 1);
console.log("1.0 === 1 =>",1.0 === 1);
console.log("1 === '1'=>",1 === '1');
console.log("1.0 === '1'=>",1.0 === '1');
// ba dấu bằng thì kiểu số và string sẽ khác nhau là false
// nghĩa là khi dùng (strictly) === thì giống nhau về chuỗi thì phải giống kiểu dữ liệu mới true
